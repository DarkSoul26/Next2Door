class DetailedProduct {
  final id;
  final productImage;
  final model;
  final description;
  final reviews;
  final price;
  final dimensions;
  final rating;

  DetailedProduct(
      {this.id,
      this.description,
      this.productImage,
      this.model,
      this.price,
      this.reviews,
      this.dimensions,
      this.rating});
}
